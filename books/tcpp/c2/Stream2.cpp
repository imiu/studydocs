# include <iostream>
using namespace std;

int main() {
    cout << "A number in decimal: " << dec << 15 << std::endl;
    cout << " in octal: " << oct << 15 << endl;
    cout << " in hex: " << hex << 15 << endl;
    cout << " a floating-point number: " << 3.141559 << endl;
    cout << " non-printing char (escape): " << char(27) << endl;
}
